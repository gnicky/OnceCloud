package com.oncecloud.newapi;

public class UserModel {
	private int userid;
	private String username;
	private String usercom;
	private String userdate;
	private int userlevel;
	private String usermail;
	private String userphone;
	private int uservoucher;

	public int getUserid() {
		return userid;
	}

	public void setUserid(int userid) {
		this.userid = userid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getUsercom() {
		return usercom;
	}

	public void setUsercom(String usercom) {
		this.usercom = usercom;
	}

	public String getUserdate() {
		return userdate;
	}

	public void setUserdate(String userdate) {
		this.userdate = userdate;
	}

	public int getUserlevel() {
		return userlevel;
	}

	public void setUserlevel(int userlevel) {
		this.userlevel = userlevel;
	}

	public String getUsermail() {
		return usermail;
	}

	public void setUsermail(String usermail) {
		this.usermail = usermail;
	}

	public String getUserphone() {
		return userphone;
	}

	public void setUserphone(String userphone) {
		this.userphone = userphone;
	}

	public int getUservoucher() {
		return uservoucher;
	}

	public void setUservoucher(int uservoucher) {
		this.uservoucher = uservoucher;
	}


}
